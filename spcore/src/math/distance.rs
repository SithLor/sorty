// _get_distance:
//         push    ebp
//         mov     ebp, esp
//         vmovsd  xmm0, qword ptr [ebp + 20]
//         vmovsd  xmm1, qword ptr [ebp + 36]
//         vsubsd  xmm0, xmm0, qword ptr [ebp + 12]
//         vsubsd  xmm1, xmm1, qword ptr [ebp + 28]
//         vmulsd  xmm0, xmm0, xmm0
//         vmulsd  xmm1, xmm1, xmm1
//         vaddsd  xmm0, xmm0, xmm1
//         vsqrtsd xmm0, xmm0, xmm0
//         mov     eax, dword ptr [ebp + 8]
//         vmovsd  qword ptr [eax], xmm0
//         pop     ebp
//         ret
#[inline(always)]
pub fn get_distance(x1: f64, x2: f64, y1: f64, y2: f64) -> f64 {
    let x = x2 - x1;
    let y = y2 - y1;
    (x * x + y * y).sqrt()
}
// get_distance_3:
//         vsubsd  xmm0, xmm1, xmm0
//         vmulsd  xmm0, xmm0, xmm0
//         vsubsd  xmm1, xmm3, xmm2
//         vmulsd  xmm1, xmm1, xmm1
//         vaddsd  xmm0, xmm0, xmm1
//         vsqrtsd xmm0, xmm0, xmm0
//         ret
#[inline(always)]
pub fn get_distance_2(x1: f64, x2: f64, y1: f64, y2: f64) -> f64 {
    let x = x2 - x1;
    let y = y2 - y1;
    (x.powf(2.0) + y.powf(2.0)).sqrt()
}
// get_distance_2:
//         vsubsd  xmm0, xmm1, xmm0
//         vsubsd  xmm1, xmm3, xmm2
//         vmulsd  xmm0, xmm0, xmm0
//         vmulsd  xmm1, xmm1, xmm1
//         vaddsd  xmm0, xmm0, xmm1
//         vsqrtsd xmm0, xmm0, xmm0
//         ret
#[inline(always)]
pub fn get_distance_3(x1:f64,x2:f64,y1:f64,y2:f64) ->f64 {
    return ((x2-x1).powf(2.0)+(y2-y1).powf(2.0)).sqrt();
}
#[inline(always)]
pub fn get_distance_small(x1: f64, x2: f64, y1: f64, y2: f64) -> f64 {
    (x2 - x1).hypot(y2 - y1)
}
